public class HTTPGetResponse {

    private String url;
    private String data;

    public HTTPGetResponse(String data, String url) {
        this.data = data;
        this.url = url;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

}
