package com.acumengroup.greekmain.chart.util;

import java.util.Date;

/**
 * * Created by Arcadia
 */
public class GreekDateFormatter {

    /**
     * @param format
     * @param date
     * @return <br>
     * <p/>
     * Get the date in custom format.
     */
    public static String format(String format, Date date) {
        return (String) android.text.format.DateFormat.format(format, date);
    }

}
